package ch02;

public class NameUnicode {
	public static void main(String[] args) {
		char aValue = '\uc6b0';
		System.out.println("char형(\'\\uc6b0\') :" + aValue);
		char bValue = '\ubcd1';
		System.out.println("char형(\'\\ubcd1\') :" + bValue);
		char cValue = '\uac78';
		System.out.println("char형(\'\\uac78\') :" + cValue);
	}
}
